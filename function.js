function tambah(a = 0, b = 0) {
    hasil = a + b;
    console.log("Hasil\t" + a + " + " + b + " = " + hasil);
}
function kurang(a = 0, b = 0) {
    hasil = a - b;
    console.log("Hasil\t" + a + " - " + b + " = " + hasil);
}
function kali(a = 0, b = 0) {
    hasil = a * b;
    console.log("Hasil\t" + a + " x " + b + " = " + hasil);
}
function bagi(a = 0, b = 0) {
    hasil = a / b;
    console.log("Hasil\t" + a + " / " + b + " = " + hasil);
}

tambah(6, 2);
kurang(6, 2);
kali(6, 2);
bagi(6, 2);